package Loop_Index_Array;

public class Array_Day11 {
    public static void main(String[] args) {
        int a [] = {1,2,3,4,5};
        for(int i = 0; i<a.length-1; i++)
        {
            System.out.println(a[i]);
            // Explanation: a.length-1 =4 so loop i from 0 to 3.
        }
    }
}
