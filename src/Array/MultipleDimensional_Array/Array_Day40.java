package Array.MultipleDimensional_Array;

public class Array_Day40 {
    public static void main(String[] args) {
        int m [] [] = {{1,2}, {3,4}};
        System.out.println(m.length);
        System.out.println(m[0].length);
        // Explanation: m.length = rows, m[0].length = columns.
    }
}
