package If_Else_Conditions;

public class Day50 {
    public static void main(String[] args) {
        int retry = 2;
        if (retry == 0)
            System.out.println("No Retry");
        else if (retry == 1)
            System.out.println("Retry Once");
        else
            System.out.println("Multiple Retries");

    }
}
