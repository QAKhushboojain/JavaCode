package If_Else_Conditions;

public class Day26 {
    public static void main(String[] args) {
        int num = 7;
        if(num % 2 == 0)
        {
            System.out.println("Even");
        }
        else {
            System.out.println("Odd");
            //  Explanation: 7 % 2 = 1 → condition false → prints Odd.
        }
    }
}
