package Operators;

public class Day_4 {
    public static void main(String[] args) {
        int i = 5;
        System.out.println(--i);
        System.out.println(i);
        // Explanation: Prefix decrement decrements first, then returns new value.
        // --i → minus first, then print
        //
        //i-- → print first, then minus
    }
}
