package Operators;
// Compound assignment (+=, -=, *=, /=, %=) and implicit cast behaviour
public class Day_5 {
    public static void main(String[] args) {
        int x = 5;
        x += 3;
        System.out.println(x);
        // Explanation: x += 3 adds and assigns (same as x = x + 3 for int).
    }


}
