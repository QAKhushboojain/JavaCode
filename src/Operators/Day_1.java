package Operators;

public class Day_1 {
    public static void main(String[] args) {
        int i = 1;
        System.out.println(i++);
        System.out.println(i);
        // Explanation: i++ returns old value (1) then increments to 2.
        // i++ → print first, then add
        //
        //++i → add first, then print
    }
}
